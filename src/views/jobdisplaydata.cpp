#include "views/jobdisplaydata.h"
//------------------------------------------------------------------------------
// Constructor(s)/Destructor
//------------------------------------------------------------------------------
JobDisplayData::JobDisplayData(){}
//------------------------------------------------------------------------------
JobDisplayData::JobDisplayData(const JobDisplayData &src){
	m_name = src.m_name;
}
//------------------------------------------------------------------------------
JobDisplayData & JobDisplayData::operator=(const JobDisplayData &src){
	if(&src != this){
		m_name = src.m_name;
	}
	return *this;
}
//------------------------------------------------------------------------------
JobDisplayData::~JobDisplayData(){}
//------------------------------------------------------------------------------
// Getters
//------------------------------------------------------------------------------
const QString & JobDisplayData::getName() const {
	return m_name;
}
//------------------------------------------------------------------------------
// Setters
//------------------------------------------------------------------------------
void JobDisplayData::setName(const QString &name){
	m_name = name;
}
